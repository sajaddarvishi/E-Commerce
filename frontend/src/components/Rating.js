// Rating.js or wherever your Rating component is located

import React from 'react';

function Rating({ value, text, color }) {
  return (
    <div className="rating" style={{ display: 'flex', justifyContent: 'flex-start', direction: 'rtl' }}>
      {/* Rating Stars */}
      <span style={{ marginRight: '0.25rem' }}>
        <i
          style={{ color }}
          className={
            value >= 1
              ? 'fas fa-star'
              : value >= 0.5
              ? 'fas fa-star-half-alt'
              : 'far fa-star'
          }
        ></i>
        <i
          style={{ color }}
          className={
            value >= 2
              ? 'fas fa-star'
              : value >= 1.5
              ? 'fas fa-star-half-alt'
              : 'far fa-star'
          }
        ></i>
        <i
          style={{ color }}
          className={
            value >= 3
              ? 'fas fa-star'
              : value >= 2.5
              ? 'fas fa-star-half-alt'
              : 'far fa-star'
          }
        ></i>
        <i
          style={{ color }}
          className={
            value >= 4
              ? 'fas fa-star'
              : value >= 3.5
              ? 'fas fa-star-half-alt'
              : 'far fa-star'
          }
        ></i>
        <i
          style={{ color }}
          className={
            value >= 5
              ? 'fas fa-star'
              : value >= 4.5
              ? 'fas fa-star-half-alt'
              : 'far fa-star'
          }
        ></i>
      </span>

      {/* Number of reviews */}
      {text && <span style={{ marginLeft: '0.5rem' }}>{text}</span>}
    </div>
  );
}

export default Rating;
